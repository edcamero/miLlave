public void mostrarComboPro(){
    try {
        c=new Conexion();
        c.ConexionPostgres();
        comboPro.removeAllItems();
        comboPro.addItem("Seleccione");     
        String query="select* from  producto";
        ResultSet resultado=c.consulta(query);
        
        while(resultado.next()){
            comboPro.addItem(resultado.getObject(1)+" "+resultado.getObject(2));
            
            
        }         
    } catch (ClassNotFoundException | SQLException | InstantiationException | IllegalAccessException ex) {
        Logger.getLogger(addProducto.class.getName()).log(Level.SEVERE, null, ex);
    }
}

