    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
     
        
        String idProducto=comboPro.getSelectedItem().toString();
        String[] vector=idProducto.split(" ");
        idProducto=vector[0];
        
        fechaI=Fecha.StringDate(fechaInicial.getDate());
        fechaF=Fecha.StringDate(fechaFinal.getDate());
        if(validar(fechaInicial.getCalendar(),fechaFinal.getCalendar())){
        try {
        c=new Conexion();
        c.ConexionPostgres();
        String query="insert into promocion (\n" +
"             id_producto, fecha_inicial, fecha_final)values('"+idProducto+"','"+fechaI+"','"+fechaF+"')";
        c.actualizar(query);
        JOptionPane.showMessageDialog(null,"Se agrego con exito");
    } catch (ClassNotFoundException | SQLException | InstantiationException | IllegalAccessException ex) {
        Logger.getLogger(addPromocion.class.getName()).log(Level.SEVERE, null, ex);
    } 
    }else{JOptionPane.showMessageDialog(null," fechas no validas");}
        limpiar();
    }//GEN-LAST:event_jButton1ActionPerformed
      
